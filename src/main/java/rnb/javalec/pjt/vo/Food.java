package rnb.javalec.pjt.vo;

public class Food {
	
	// 식당번호
	private int fno;

	//식당이름
	private String fname;
	
	//식당 지역
	private String floc;

	public int getFno() {
		return fno;
	}

	public void setFno(int fno) {
		this.fno = fno;
	}

	public String getFname() {
		return fname;
	}

	public void setFname(String fname) {
		this.fname = fname;
	}

	public String getFloc() {
		return floc;
	}

	public void setFloc(String floc) {
		this.floc = floc;
	}
	
	
	
}
